public class ruut {
    static void ruut_y(double r){
        double d = 2*r;
        double ans = 4*d;
        System.out.printf("Ruudu ümbermõõt on: %.1f\n", ans);
    }
    static void ruut_p(double r){
        double d = 2*r;
        double ans = d*d;
        System.out.printf("Ruudu pindala on: %.1f\n", ans);
    }
}
